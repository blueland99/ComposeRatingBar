package com.blueland.rating_bar

/**
 * 도형의 종류를 나타내는 enum 클래스입니다.
 * 이 클래스는 그릴 수 있는 도형의 형태를 정의합니다.
 *
 * This enum class represents the type of shapes.
 * It defines the possible shapes that can be drawn.
 *
 * @property Star 별 도형
 * @property Circle 원형 도형
 * @property Square 사각형 도형
 * @property Heart 하트 도형
 */
enum class ShapeType {
    Star, Circle, Square, Heart
}
